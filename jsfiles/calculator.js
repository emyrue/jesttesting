class calculator {
  static add(number1, number2) {
    return number1 + number2;
  }

  static subtract(number1, number2) {
    return number1 - number2;
  }

  static divide(number1, number2) {
    return number1 / number2;
  }

  static multiply(number1, number2) {
    return number1 * number2;
  }
}

module.exports = calculator;